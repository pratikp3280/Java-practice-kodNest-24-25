package Multi_Dimensional_arrays;
import java.util.Scanner;

public class First_Array {

	public static void main(String[] args) {
		//Creating an array called marks and Assigning it Values 
		int[] marks = {35 , 45 , 55 , 65 , 75 };
		
//		Creating array
		int[] grades = new int [6];
		grades[0] = 25;
		grades[1] = 25;
		grades[2] = 25;
		grades[3] = 25;
		
		System.out.println("Printing the Array value of index=4 of array called marks :");
		System.out.println(marks[4]);
		System.out.println("Printing the Array value of index=2 of array called grades :");
		System.out.println(grades[2]);

	}

}
