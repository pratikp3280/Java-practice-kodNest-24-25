
class Class_Have_Static {
	

		//static variable
		static int x = 100;
		
		//static method
		public static void fun1(){
			System.out.println("Static method is executing");
		}
		
		//non-static variable
		int y = 200;
		
		//non-static method
		public void fun2(){
			System.out.println("Non-Static method is executing");
		}
	
		
}



