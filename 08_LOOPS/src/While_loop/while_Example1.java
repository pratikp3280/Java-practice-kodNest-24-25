package While_loop;

public class while_Example1 {

	public static void main(String[] args) {
		int i = 1;    //initializing i value
		int j = 10;	// initializing j Value
		
		while(i<=j) {
            System.out.println("i:"+ i);				//prints i value
			System.out.println("j:"+ j);				//prints j value
			System.out.println();
			
			i++;      //incrementing the value of i after every printing
		}
		

	}

}
